setTimeout(function() {
    var errorPopup = document.getElementById("errorPopup");
    if (errorPopup) {
        errorPopup.style.display = "none";
    }
}, 2000);

setTimeout(function() {
    var successPopup = document.getElementById("successPopup");
    if (successPopup) {
        successPopup.style.display = "none";
    }
}, 2000);
